public class Konstanta {
    /* Format penulisan constanta di Java
        <access_modifier> static final <tipe_data> <NAMA_KONSTANTA>= nilai_konstanta;
         */

    public static final String INI_KONSTANTA_STRING = "INI KONSTANTA STRING";
    public static final int ANGKA_INT = 20;
    public static final double INI_DOUBLE = 29.3;


    public static void main(String[] args) {
        //Panggil konstanta
        System.out.println(INI_KONSTANTA_STRING);
        System.out.println(ANGKA_INT);
        System.out.println(INI_DOUBLE);


    }
}
