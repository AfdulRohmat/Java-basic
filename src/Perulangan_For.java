public class Perulangan_For {
    public static void main(String[] args) {
        /*
        Struktur perulangan (atau dalam bahasa inggris disebut loop)
        adalah instruksi kode program yang bertujuan untuk mengulang
        beberapa baris perintah.
         */

        /*
            for (init statement; condition; post statemen//increment//decrement) {
               // kode program
               // kode program
            }
         */

        for (int i = 0; i <= 10; i++){
            System.out.println(i);
        }

        for(int i=1; i<=10; i++){
            System.out.println(i*3);
        }


    }
}
